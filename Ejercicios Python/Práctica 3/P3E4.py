# Pide al usuario que introduzca 3 calificaciones y calcule la media
nota_matematicas = float(input('Introduce tu nota de matematicas: '))
nota_castellano = float(input('Introduce tu nota de castellano: '))
nota_informatica = float(input('Introduce tu nota de informatica: '))
nota_media = (nota_matematicas + nota_castellano + nota_informatica)/3
print('Tu nota media es:', nota_media)

