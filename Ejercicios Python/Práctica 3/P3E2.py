# Pida espacio recorrido por coche y el tiempo tardado, y calcule velocidad media
km = float(input('Introduce los km recorridos por tu coche: '))
horas = float(input('Introduce el tiempo que ha tardado: '))
velocidad_media = km / horas
print(f'Si tu coche ha recorrido {km} km en {horas} horas, su velocidad media ha sido de {velocidad_media} km por hora.')