# Diagrama para calcular y mostrar el área de un triángulo
base = float(input('Introduce base del triángulo: '))
altura = float(input('Introduce la altura del triángulo: '))
area = (base*altura)/2
print('Si la base mide:', base, 'y la altura mide:', altura)
print('El area del triángulo es:', area)